    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (valida()) {
            if (editar == true) {
                CadastroFornecedores2 c = new CadastroFornecedores2(txtCNPJ.getText(), txtNome.getText(), txtRazao.getText(), txtEmail.getText(), txtTelefone.getText(), txtCelular.getText(), true);
                c.setVisible(true);
                this.dispose();
            } else {
                new CadastroFornecedores2(txtCNPJ.getText(), txtNome.getText(), txtRazao.getText(), txtEmail.getText(), txtTelefone.getText(), txtCelular.getText()).setVisible(true);
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Digite todos os dados corretamente!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void txtCNPJFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCNPJFocusLost
        String cnpj = txtCNPJ.getText().replace(".", "").replace("-", "").replace("/", "");
        if (Validacao.isValidCNPJ(cnpj)) {
            txtCNPJ.setBorder(new MatteBorder(new Insets(1, 1, 1, 1), Color.gray));
            btnSalvar.setEnabled(true);
            StringBuilder b = new StringBuilder(cnpj);
            b.insert(cnpj.length() - 2, "-").insert(cnpj.length() - 6, "/").insert(cnpj.length() - 9, ".").insert(cnpj.length() - 12, ".");
            txtCNPJ.setText(b.toString());
            btnSalvar.setEnabled(true);
            lblErro.setVisible(false);
        } else {
            txtCNPJ.setBorder(new MatteBorder(new Insets(1, 1, 1, 1), new Color(173, 43, 47)));
            btnSalvar.setEnabled(false);
            lblErro.setVisible(true);
        }
    }//GEN-LAST:event_txtCNPJFocusLost

