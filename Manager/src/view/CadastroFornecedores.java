    public boolean valida() {
        return txtCNPJ.getText().length() > 1 && txtNome.getText().length() > 0 && txtRazao.getText().length() > 1 && txtEmail.getText().length() > 8
                && txtEmail.getText().contains("@") && txtCelular.getText().trim().length() > 14;
    }

    public void limpa() {
        if (txtCNPJ.isEnabled()) {
            txtCNPJ.setText("");
        }

        txtNome.setText("");
        txtRazao.setText("");
        txtEmail.setText("");
        txtTelefone.setText("");
        txtCelular.setText("");
        txtTelefone = new JFormattedTextField("(##) ####-####");
        txtCelular = new JFormattedTextField("(##) #####-####");
    }

