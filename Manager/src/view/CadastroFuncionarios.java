    private void lblNovoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblNovoMouseClicked

        String cargo = JOptionPane.showInputDialog(null, "Qual é o novo cargo?", "Cadastro de cargo", JOptionPane.QUESTION_MESSAGE);
        if (cargo == null || cargo.equals("")) {
            return;
        }

        if (new CargoController().salvar(cargo)) {
            listaCargos();
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

        } else {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar, verifique se digitou corretamente!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_lblNovoMouseClicked

    private void txtCPFFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCPFFocusLost
        String cpf = txtCPF.getText().replace(".", "").replace("-", "");
        if (Validacao.isValidCPF(cpf)) {
            txtCPF.setBorder(new MatteBorder(new Insets(1, 1, 1, 1), Color.gray));
            btnSalvar.setEnabled(true);
            StringBuilder b = new StringBuilder(cpf);
            b.insert(cpf.length() - 2, "-").insert(cpf.length() - 5, ".").insert(cpf.length() - 8, ".");
            txtCPF.setText(b.toString());
        } else {
            txtCPF.setBorder(new MatteBorder(new Insets(1, 1, 1, 1), new Color(173, 43, 47)));
            btnSalvar.setEnabled(false);
        }
    }//GEN-LAST:event_txtCPFFocusLost

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (validar()) {
            new CadastroFuncionarios2(txtCPF.getText(), txtNome.getText(), txtSobrenome.getText(), txtData.getDate(), txtEmail.getText(), txtTelefone.getText(), txtCelular.getText(),
                     cmbCargo.getSelectedItem().toString(), Float.parseFloat(txtSalario.getText())).setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Não foi possivel validar os campos\nVerifique se digitou tudo corretamente!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

