    private void puxar(String cpf) {
        Funcionario c = new FuncionarioController().puxar(cpf);
        txtCPF.setText(c.getCpf());
        txtNome.setText(c.getNome());
        txtSobrenome.setText(c.getSobrenome());
        txtEmail.setText(c.getEmail());
        txtData.setDate(c.getDataAdmissao());
        txtSalario.setText(c.getSalario() + "");
        txtTelefone.setText(c.getTelefone());
        txtCelular.setText(c.getCelular());
        cmbCargo.setSelectedItem(c.getCargo().getCargo());
    }

    private void listaCargos() {
        cmbCargo.removeAllItems();
        List<Cargo> cargos = new CargoController().listar();
        for (Cargo cargo : cargos) {
            cmbCargo.addItem(cargo.getCargo());
        }
    }

    private boolean validar() {
        return txtNome.getText().length() > 0 && txtSobrenome.getText().length() > 1 && txtEmail.getText().length() > 8
                && txtEmail.getText().contains("@") && txtCelular.getText().trim().length() > 14 && txtData.getDateFormatString().length() == 10 && txtSalario.getText().length() > 0;
    }

    private void limpar() {
        if (txtCPF.isEnabled()) {
            txtCPF.setText("");
        }
        txtNome.setText("");
        txtSobrenome.setText("");
        txtEmail.setText("");
        txtTelefone.setText("");
        txtTelefone = new JFormattedTextField("(##) ####-####");
        txtCelular.setText("");
        txtCelular = new JFormattedTextField(("(##) #####-####"));
        cmbCargo.setSelectedIndex(0);
        txtData.setDateFormatString("");
        txtSalario.setText("");
    }
