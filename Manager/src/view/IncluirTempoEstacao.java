    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:

        if ("".equals(txtUser.getText()) || txtUser.getText() == null) {
            JOptionPane.showMessageDialog(null, "Informe um nome de Usuário corretamente.");

        } else {
            int userId = new UsuarioDAO().getUserId(txtUser.getText());

            BancoDeHoras bancohoras = new BancoDeHorasDAO().selectByUser(userId);

            if (bancohoras.getTempo() > 0) {
                this.horaBanco = bancohoras.getHora();
                this.minutoBanco = bancohoras.getMinuto();
                this.segundoBanco = bancohoras.getSegundo();

                lblTempo.setText(formatHora(horaBanco, minutoBanco, segundoBanco));

            } else {
                JOptionPane.showMessageDialog(null, "O Cliente possui 00:00:00 em nosso banco de horas");
            }
        }


    }//GEN-LAST:event_btnConsultarActionPerformed

