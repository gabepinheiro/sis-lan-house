    private void setValues(int hora, int min, int sec) {
        this.hora = hora + this.horaBanco;
        this.minuto = min + this.minutoBanco;
        this.segundo = sec + this.segundoBanco;

        lblTempo.setText(formatHora(this.hora, this.minuto, this.segundo));

    }
    
    private void resetValues(){
        this.hora = 00;  this.horaBanco = 00;
        this.minuto = 00;  this.minutoBanco = 00;
        this.segundo = 00;  this.segundoBanco = 00;

        lblTempo.setText("00:00:00");
    }

    public String formatHora(int hora, int min, int sec) {
        String value = "";
        if (hora < 10) {
            value = "0" + hora;
        } else {
            value = hora + "";
        }

        if (min < 10) {
            value += ":0" + min;
        } else {
            value += ":" + min;
        }

        if (sec < 10) {
            value += ":0" + sec;
        } else {
            value += ":" + sec;
        }

        return value;
    }

